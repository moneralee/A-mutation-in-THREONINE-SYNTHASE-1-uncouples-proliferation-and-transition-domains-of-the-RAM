iteration<-40 #time where RAM still present in mto2-2
files<-c("WT","mto22")#,"double mutant","ts2")#,"mto22-cc2")
rootThr<-0
mega=matrix(nrow=1000000,ncol=4,0)
contador<-0
variablethr<-0
normalizador<-0
for(i in 1:length(files)){
  simul<-files[i]
  a<-read.csv(paste(name,simul,"/",simul,"-time-",iteration,"a.u..csv",sep=""),header = F)
  dimensions<-dim(a)
  dimensions<-dimensions[1]
  a<-sortcoord(a,dimensions)
  for(j in 1:dimensions){
    if(a[j,2]>22){ #Columella not considered
      contador<-contador+1
      mega[contador,1]<-files[i]
      mega[contador,2]<-a[j,17]
      if((a[j,2]==29) | (a[j,2]==23)){
        mega[contador,3]<-"SCN"
      }else if(a[j,2]>160){mega[contador,3]<-"Base"
      }else{mega[contador,3]<-"Apex"}
    }
  }
  if(files[i]=="WT"){normalizador<-a[1,17]}   #normalise with value of most apical cell in Wt (SC)
  rootThr[i]<-sum(a[,17])+sum(a[,12])
}
mega[1:contador,4]<-as.integer(mega[1:contador,2])/normalizador
