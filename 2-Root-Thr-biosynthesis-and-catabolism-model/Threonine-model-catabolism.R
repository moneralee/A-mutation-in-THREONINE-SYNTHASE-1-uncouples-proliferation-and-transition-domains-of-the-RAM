# 2- Cell length comparison, Wt domains and RAM  mto2-2
files<-c("WT","mto22")
simulationtime<-50
tComparison=matrix(ncol=simulationtime,nrow=2,1)
WtPD<-0
WtTD<-0
mto22RAM<-0
# Positions:
#Wt PD 29-108
#Wt TD 108-160
#mto22 RAM 29-160
for(i in 1:simulationtime){
  contWtTD<-0
  contWtPD<-0
  contmto22<-0
  for(simul in files){ #create vectors to be compared
    data<-read.csv(paste("output/",simul,"/",simul,"-time-",i,"a.u..csv",sep=""))
    dimenshions<-dim(data)
    dimenshions<-dimenshions[1]
    for(j in 1:dimenshions){
      if((data[j,3]<160) & data[j,2]>29){ #RAM
        if(simul=="mto22"){
          contmto22<-contmto22+1
          mto22RAM[contmto22]<-data[j,3]-data[j,2]
        }else{
          if(data[j,2]>108){
            contWtTD<-contWtTD+1
            WtTD[contWtTD]<-data[j,3]-data[j,2]
          }else{
            contWtPD<-contWtPD+1
            WtPD[contWtPD]<-data[j,3]-data[j,2]
          }
        }
      }
    }
  }
  if(length(mto22RAM)>1){
    if(length(WtPD)>1){
      a<-t.test(mto22RAM, WtPD,alternative = "two.sided",conf.level = 0.95)
      tComparison[1,i]<-a$p.value}
    if(length(WtTD)>1){
      a<-t.test(mto22RAM, WtTD,alternative = "two.sided",conf.level = 0.95)
      tComparison[2,i]<-a$p.value}
  }
}
png("output/TD-RAM-comparison.png",width = 1200,height = 400)
plot(tComparison[1,],ylab="p-value",xlab="simulation time",xlim=c(10,50))
lines(tComparison[1,])
points(tComparison[2,],col="red")  #TD Wt vs RAM mto22
lines(tComparison[2,],col="red")  
points(tComparison[1,],col="black")  #PD Wt vs RAM mto22
lines(tComparison[1,],col="black")   
legend(45,1,legend=c("TD WT vs RAM mto22", "PD WT vs RAM mto22"),col=c("red","black"),pch=15)
abline(h=0.001,col="blue")
dev.off()

